export const getTitleByOptions = (options, val) => {
    for (const option of options) {
        if (Array.isArray(option)) {
            for (const innerOption of option) {
                if (innerOption.value === val) {
                    return innerOptions.title;
                }
            }
        } else {
            if (option.value === val) {
                return option.title;
            }
        }
    }

    return "";
};
